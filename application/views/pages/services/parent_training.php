<h3>Behavioral and Emotional Skills Training (BEST) </h3>
<p>It is an interactive training program for parents to help manage problematic behaviors at home. By incorporating effective behavior management techniques, this program lets the parents and caregivers comprehensively deal with children having behavioral problems. Parents learn all the basics of behavior management and apply their skills effectively and consistently to augment their confidence in managing challenging behaviors.</p>
<h3>The Incredible Years</h3>
<p>This program is a group-based training for parents of infants and children up to 12 years. There are four groups – baby, toddler, preschool, and school age. The duration of the program is around 3 to 5 weeks.</p>

<p>In addition, we also offer specialized training for parents with children having oppositional defiance disorder and ADHD. The program aims to improve parent-child relationships and positive attachment. It also covers consistent routines, limit-setting, and rules and strategies such as problem-solving, time to calm down, redirection, ignoring, logical and natural consequences.</p>
<h3>Positive Parenting Program (Triple P)</h3>
<p>The objective of this program is to impart the necessary skills and information to parents to improve their self-sufficiency and confidence in managing the problematic behavior of their children. Parents can implement their skills to manage a wide age range of children. The program is completed in different sessions with different levels of intervention.</p>
<h3>Defiant Teens</h3>
<p>This program teaches more effective strategies and tools to parents that help in dealing with defiant or noncompliance behavior and also effectively interacting with their teenager. In the first part of the program, only parents are involved while in the second part, both parents and teenagers are involved in problem-solving communication. The objective of this program is to impart effective negotiation, problem-solving and communication skills.</p>
<h3>Parent Management Training (PMT)</h3>
<p>Therapists at Dr. Habib’s Foster CDC teach and model some necessary skills to deal more effectively with challenging behaviors. Parents then practice these skills at home. There are almost 10 sessions in this program.</p>
<h3>Parent-Child Interaction Therapy (PCIT) </h3>
<p>Parents learn skills to interact in a positive and productive way with their children. Under the supervision of an expert therapist, parents, and their child perform a variety of tasks and practice specific responses to both desired and undesired behaviors. PCIT is good for children between 2 and 7 years of age. The duration of the therapy is around 3 to 4 months. It is the most practice-intensive training that lets parents gain mastery of each skill before going on to the next one.</p>
